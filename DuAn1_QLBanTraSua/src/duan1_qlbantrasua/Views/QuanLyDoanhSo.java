    private void loadTableTK() {
        DefaultTableModel model = (DefaultTableModel) this.tblTKSP.getModel();
        model.setRowCount(0);
        for (ThongKeSanPham tk : this.sps.getListTKSanPham()) {
            Object[] rowData = {
                tk.getMa(),
                tk.getTen(),
                tk.getGia(),
                tk.getSoLuongBan(),
                tk.getDoanhThu(tk.getGia(), tk.getSoLuongBan()),
                tk.getTrangThai()
            };

            model.addRow(rowData);
        }
    }
